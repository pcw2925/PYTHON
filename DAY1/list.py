
scores = [85,92,98,71]
print(scores)

print(scores[1])
print(scores[2])
print(scores[-1])

print(len(scores))

scores[2] = 88
print(scores[2])
print(scores)

# append 추가
scores.append(90)
print(scores)
# remove 제거
scores.remove(88)
print(scores)

# 삽입
scores.insert(3,100)
print(scores)

# pop 반환과 제거를 동시
scores.pop(1)
print(scores)

# 오름차순
scores.sort()
print(scores)

# 내림차순
scores.sort(reverse=True)
print(scores)

# 모든 항목 지우기
scores.clear()
print(scores)

# 값을 절대로 바꾸면 안될때는 튜플을 사용
# 값을 바꿔아 할때면 리스트를 사용함


